package mobapptut.com.camera2videoimage;

public class PlantInfo {
        private String id;
        private String name;
        private int height;

        public PlantInfo(String name) {
            this.name = name;
        }

        public String getId() {
            return id;
        }

        public String getName() {
            return name;
        }

        public int getHeight() {
            return height;
        }

    }
